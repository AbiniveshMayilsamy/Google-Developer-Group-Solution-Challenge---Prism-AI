// ── Cloud Storage: signed URL or direct upload ───────────────────────────────────
router.post("/gcs/upload", protect, async (req, res) => {
  const { auditId, artifactType, fileName, content } = req.body;
  const ext =
    artifactType === "csv" ? "csv" : artifactType === "pdf" ? "pdf" : "json";
  const name = fileName || `audit-${auditId || "unknown"}.${ext}`;
  const objectPath = `audits/${new Date().toISOString().split("T")[0].replace(/-/g, "/")}/${name}`;

  if (content) {
    const contentType =
      ext === "csv"
        ? "text/csv"
        : ext === "pdf"
          ? "application/pdf"
          : "application/json";
    const uploadResult = await uploadToGcs(objectPath, content, contentType);
    return res.json({ ...uploadResult, object: objectPath });
  }

  const result = await generateSignedUploadUrl(objectPath);
  res.json({ ...result, object: objectPath });
});

