// ── Pub/Sub: publish bias event ────────────────────────────────────────────────
router.post("/pubsub/publish", protect, async (req, res) => {
  const { auditId, auditData, eventType, metrics, config } = req.body;
  let payload = req.body.payload;

  if (!payload) {
    let audit = auditData;
    if (auditId) {
      const found = await Audit.findById(auditId);
      if (!found) return res.status(404).json({ error: "Audit not found" });
      audit = found.toObject();
    }
    payload = {
      type: eventType || "bias_firewall.alert",
      auditId: audit?._id || auditId || "inline",
      datasetName: audit?.datasetName || config?.datasetName || "Unknown",
      disparateImpact: audit?.metrics?.disparateImpact ?? metrics?.disparateImpact,
      statisticalParityDifference:
        audit?.metrics?.statisticalParity ??
        audit?.metrics?.statisticalParityDifference ??
        metrics?.statisticalParityDifference,
      status: audit?.status || "Unknown",
      action: "review_required",
      timestamp: new Date().toISOString(),
    };
  }

  const result = await publishPubSubMessage(payload, req.body.topic);
  res.json({ ...result, payload });
});

