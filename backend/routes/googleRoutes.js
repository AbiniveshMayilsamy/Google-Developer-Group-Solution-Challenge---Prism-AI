// ── POST dispatch audit to all configured Google services ────────────────────────
router.post("/dispatch", protect, async (req, res) => {
  try {
    const { services, recipient } = req.body;
    const resolved = await resolveAudit(req);
    if (resolved.error) return res.status(resolved.status).json({ error: resolved.error });
    if (!resolved.audit) {
      return res.status(400).json({ error: "auditId or auditData is required" });
    }

    const result = await dispatchAuditToGoogleServices(resolved.audit, {
      services: services || ["pubsub", "bigquery", "logging", "chat", "gcs"],
      recipient: recipient || resolved.user?.email,
      accessToken: getAccessToken(req),
      chatCardBuilder: (a) =>
        buildChatCard({
          datasetName: a.datasetName,
          disparateImpact: a.metrics?.disparateImpact,
          spd: a.metrics?.statisticalParity ?? a.metrics?.statisticalParityDifference,
          status: a.status,
          dashboardUrl: req.body.dashboardUrl || `${req.headers.origin || "http://localhost:5173"}/analyze/results`,
        }),
      logEntryBuilder: (a) => buildLogEntry(a),
    });

    res.json(result);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

